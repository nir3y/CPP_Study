#include <iostream>
#include <string>
using namespace std;

class Account 
{
	string name;
	int id;
	int balance;

public:
	Account(string n, int i, int bal);
	void deposit(int money);
	string getOwner();//계좌의 주인 
	int withdraw(int money);//잔액
	int inquiry();//남은 잔액

};

Account::Account(string n , int i , int bal)
{
	name = n; id = i; balance = bal;
}

void Account::deposit(int money)
{
	balance += money;

}

string Account::getOwner()
{
	return name;
}

int Account::withdraw(int money)
{
	if (money > balance)
	{
		money = balance; // 출금을 남은 금액만큼만 할 수 있으니.
		balance = 0;
	}

	balance -= money;

	return money;
}

int Account::inquiry()
{
	return balance;
}


int main() {
	Account a("kitae", 1, 5000); // id 1번, 잔액 5000원, 이름이 kitae인 계좌 생성
	a.deposit(50000);// 50000원 저금
	cout << a.getOwner() << "의 잔액은 " << a.inquiry() << endl;
	int money = a.withdraw(20000);// 20000원 출금. withdraw()는 출금한 실제 금액 리턴
	cout << a.getOwner() << "의 잔액은 " << a.inquiry() << endl;
	system("pause");
	return 0;
}
